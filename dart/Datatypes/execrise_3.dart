// variable are containers that holds different that datatype
void main() {
  // how to declare varibales
  // <datatype> <variablename> = value;

  // Integers
  int a = 3;
  int b = 2;
  print(a + b);
  print(a - b);
  print(a * b);

  // Double
  double d = 3.3;
  double e = 3.4;
  print(d + e);
  print(d - e);
  print(d * e);
  // String
  String name = "fred";
  String learn = " is learning dart programming";
  print(name + learn);

  // Boolean
  bool isN = true;
  print(isN);

  // dynamic means can take any datatypes
  dynamic somevalue = "true";
  dynamic value = 13;

  print(somevalue);
  print(value);
}
